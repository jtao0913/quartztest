package com.cnipr.cniprgz.entity;

public class IprGroup {
	private int intGroupId;
	private String chrGroupName;
	private String chrChannelId;
	private String chrItemGrant;
	private String chrGroupMemo;
	public int getIntGroupId() {
		return intGroupId;
	}
	public void setIntGroupId(int intGroupId) {
		this.intGroupId = intGroupId;
	}
	public String getChrGroupName() {
		return chrGroupName;
	}
	public void setChrGroupName(String chrGroupName) {
		this.chrGroupName = chrGroupName;
	}
	public String getChrChannelId() {
		return chrChannelId;
	}
	public void setChrChannelId(String chrChannelId) {
		this.chrChannelId = chrChannelId;
	}
	public String getChrItemGrant() {
		return chrItemGrant;
	}
	public void setChrItemGrant(String chrItemGrant) {
		this.chrItemGrant = chrItemGrant;
	}
	public String getChrGroupMemo() {
		return chrGroupMemo;
	}
	public void setChrGroupMemo(String chrGroupMemo) {
		this.chrGroupMemo = chrGroupMemo;
	}
  
}
